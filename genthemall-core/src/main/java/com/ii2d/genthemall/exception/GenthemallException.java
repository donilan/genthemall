package com.ii2d.genthemall.exception;

public class GenthemallException extends RuntimeException {

	private static final long serialVersionUID = 1L;

	public GenthemallException() {
		super();
	}

	public GenthemallException(String message, Throwable cause) {
		super(message, cause);
	}

	public GenthemallException(String message) {
		super(message);
	}

	public GenthemallException(Throwable cause) {
		super(cause);
	}

	
}
